import * as React from "react";

import {
  Create,
  SimpleForm,
  CreateProps,
  TextInput,
  ReferenceArrayInput,
  SelectArrayInput,
} from "react-admin";

import { QuestObjectiveTitle } from "../questObjective/QuestObjectiveTitle";
import { QuestPoiTitle } from "../questPoi/QuestPoiTitle";

export const QuestRegionCreate = (props: CreateProps): React.ReactElement => {
  return (
    <Create {...props}>
      <SimpleForm>
        <TextInput label="Name" source="name" />
        <ReferenceArrayInput
          source="questObjectives"
          reference="QuestObjective"
        >
          <SelectArrayInput
            optionText={QuestObjectiveTitle}
            parse={(value: any) => value && value.map((v: any) => ({ id: v }))}
            format={(value: any) => value && value.map((v: any) => v.id)}
          />
        </ReferenceArrayInput>
        <ReferenceArrayInput source="questPoiPoints" reference="QuestPoi">
          <SelectArrayInput
            optionText={QuestPoiTitle}
            parse={(value: any) => value && value.map((v: any) => ({ id: v }))}
            format={(value: any) => value && value.map((v: any) => v.id)}
          />
        </ReferenceArrayInput>
      </SimpleForm>
    </Create>
  );
};
